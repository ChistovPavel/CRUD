package XML;

/**
 * Enum класс содержит имена групп XML файла хранилища
 * */
public enum SupportGroups {
    /**
     * Имя группы для хранения информации об имени пользователя
     * */
    firstNameGroup,
    /**
     * Имя группы для хранения информации о фамилии пользователя
     * */
    secondNameGroup,
    /**
     * Имя группы для хранения информации о дате рождения пользователя
     * */
    birthDateGroup;
}
